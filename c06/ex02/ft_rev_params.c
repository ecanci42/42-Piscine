#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	x;

	i = ac -1;
	while (i > 0)
	{
		x = 0;
		while (av[i][x])
		{
			write (1, &av[i][x], 1);
			x++;
		}
		write (1, "\n", 1);
		i--;
	}
	return (0);
}
