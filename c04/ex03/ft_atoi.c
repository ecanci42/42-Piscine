int	ft_atoi(char *str)
{
	int	i;
	int	x;
	int	c;

	i = 0;
	x = 1;
	c = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			x *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = c * 10 + str[i] - 48;
		i++;
	}
	return (x * c);
}
