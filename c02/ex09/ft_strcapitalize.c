char	*ft_strcapitalize(char *str)
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (str[i])
	{
		if (x == 0 && (str[i] >= 'a' && str[i] <= 'z'))
		{
			str[i] -= 32;
			x++;
		}
		else if (x > 0 && (str[i] >= 'A' && str[i] <= 'Z'))
			str[i] += 32;
		else if (str[i] < 48 || (str[i] > 57 && str[i] < 65)
			|| (str[i] > 90 && str[i] < 97) || str[i] > 122)
			x = 0;
		else
			x++;
		i++;
	}
	return (str);
}
